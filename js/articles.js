function openArticle(key) {
    const a = articles[key];
    if (!a) return;
    const hero = document.getElementById('modalHero');
    hero.src = a.hero;
    hero.onerror = () => { hero.src = a.heroBak; };
    document.getElementById('modalCat').textContent = a.cat;
    document.getElementById('modalTitle').textContent = a.title;
    document.getElementById('modalMeta').innerHTML = `<span>${a.date}</span><span>·</span><span>${a.read}</span>`;
    document.getElementById('modalContent').innerHTML = a.content;
    document.getElementById('articleModal').scrollTop = 0;
    document.getElementById('articleModalOverlay').classList.add('open');
    document.body.style.overflow = 'hidden';
}

function closeArticle() {
    document.getElementById('articleModalOverlay').classList.remove('open');
    document.body.style.overflow = '';
}

function handleOverlayClick(e) {
    if (e.target === document.getElementById('articleModalOverlay')) closeArticle();
}

function openAllArticles() {
    var cards = '';
    var keys = Object.keys(articles);
    for (var i = 0; i < keys.length; i++) {
        var key = keys[i];
        var art = articles[key];
        cards += '<div'
            + ' onclick="openArticle(\'' + key + '\')"'
            + ' onmouseover="this.style.borderColor=\'var(--orange)\'"'
            + ' onmouseout="this.style.borderColor=\'var(--border)\'"'
            + ' style="background:var(--dark4);border:1px solid var(--border);border-radius:10px;'
            + 'padding:1.2rem 1.5rem;cursor:pointer;transition:border-color 0.2s;margin-bottom:0.2rem;">'
            + '<div style="font-family:\'Barlow Condensed\',sans-serif;font-size:0.7rem;font-weight:700;'
            + 'letter-spacing:0.2em;text-transform:uppercase;color:var(--orange);margin-bottom:0.4rem;">'
            + art.cat + '</div>'
            + '<div style="font-family:\'Barlow Condensed\',sans-serif;font-size:1.1rem;font-weight:700;'
            + 'text-transform:uppercase;color:var(--white);margin-bottom:0.5rem;">'
            + art.title + '</div>'
            + '<div style="font-size:0.78rem;color:var(--muted);">'
            + art.date + ' &middot; ' + art.read + '</div>'
            + '</div>';
    }
    var content = '<p>Browse our full collection of industry insights, technical guides, and product spotlights from the IPATECHS team.</p>'
        + '<div style="display:flex;flex-direction:column;gap:1rem;margin-top:1.5rem;">'
        + cards
        + '</div>';

    document.getElementById('modalHero').src = 'https://images.unsplash.com/photo-1504328345606-18bbc8c9d7d1?w=780&q=80';
    document.getElementById('modalCat').textContent = 'All Articles';
    document.getElementById('modalTitle').textContent = 'Industry Insights';
    document.getElementById('modalMeta').innerHTML = '';
    document.getElementById('modalContent').innerHTML = content;
    document.getElementById('articleModal').scrollTop = 0;
    document.getElementById('articleModalOverlay').classList.add('open');
    document.body.style.overflow = 'hidden';
}

